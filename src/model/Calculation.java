package model;

public class Calculation {
    private double nachisleno;
    private String fam;
    private double tax;
    private  double vidach;


    public double getNachisleno() {
        return nachisleno;
    }

    public void setNachisleno(double nachisleno) {
        this.nachisleno = nachisleno;
    }

    public String getFam() {
        return fam;
    }

    public void setFam(String fam) {
        this.fam = fam;
    }

    public double getTax() {
        return tax;
    }

    public void setTax(double tax) {
        this.tax = tax;
    }

    public double getVidach() {
        return vidach;
    }

    public void setVidach(double vidach) {
        this.vidach = vidach;
    }
}
