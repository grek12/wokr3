package model;

public class Sotr {
    private int id;
    private String fam;
    private double oklad;
    private int otrday;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFam() {
        return fam;
    }

    public void setFam(String fam) {
        this.fam = fam;
    }

    public double getOklad() {
        return oklad;
    }

    public void setOklad(double oklad) {
        this.oklad = oklad;
    }

    public int getOtrday() {
        return otrday;
    }

    public void setOtrday(int otrday) {
        this.otrday = otrday;
    }
}